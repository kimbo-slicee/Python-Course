# character_list.py

"""
This file contains a list of commonly used characters in Python
and explains the role or meaning of each one.
"""

# -------------------------------
# LETTER CHARACTERS
# -------------------------------

a = 'a'       # Lowercase letter
Z = 'Z'       # Uppercase letter

# Letters can be used in strings or variable names (as long as they don't start with a digit).


# -------------------------------
# DIGIT CHARACTERS
# -------------------------------

zero = '0'    # Digit zero (as a character)
nine = '9'    # Digit nine (as a character)

# Digits can be part of strings or numbers, but variables can't start with them.


# -------------------------------
# WHITESPACE CHARACTERS
# -------------------------------

space = ' '       # Space character
tab = '\t'        # Tab character
newline = '\n'    # Newline character (line break)

# These are used for formatting text in strings.


# -------------------------------
# SYMBOL CHARACTERS
# -------------------------------

plus = '+'           # Addition or string concatenation
minus = '-'          # Subtraction or negative sign
asterisk = '*'       # Multiplication or repetition
slash = '/'          # Division
backslash = '\\'     # Escape character
equal = '='          # Assignment or comparison (==)
less_than = '<'      # Comparison operator
greater_than = '>'   # Comparison operator
exclamation = '!'    # Used in '!=' (not equal)
percent = '%'        # Modulo (remainder)
caret = '^'          # Bitwise XOR
ampersand = '&'      # Bitwise AND
pipe = '|'           # Bitwise OR
tilde = '~'          # Bitwise NOT
at = '@'             # Used in decorators

# These characters are used in operations or syntax.


# -------------------------------
# PUNCTUATION CHARACTERS
# -------------------------------

comma = ','          # Separates values (e.g., in lists or arguments)
colon = ':'          # Used in dictionaries, loops, and function definitions
semicolon = ';'      # Allows multiple statements on one line (rare in Python)
dot = '.'            # Access attributes/methods or floats
quote = '"'          # Used for strings
apostrophe = "'"     # Also used for strings

# Punctuation is used to define structure or data in Python.


# -------------------------------
# BRACKETS AND PARENTHESES
# -------------------------------

parentheses = '()'   # Used in functions and tuples
square_brackets = '[]'  # Used for lists and indexing
curly_braces = '{}'  # Used for dictionaries and sets

# Brackets group values or control code execution.


# -------------------------------
# OTHER CHARACTERS
# -------------------------------

underscore = '_'     # Used in variable names or to ignore values
hash_char = '#'      # Starts a comment

# The underscore is commonly used for naming or ignoring values in unpacking.

#-----------------------------
# Escape Sequences Characters
#-----------------------------
back_space="\b"
print("remove \bspace")
scape_new_line="\\"
print("Hello in my \
python\
Course") # \ scape the new line and tell the interpreter it is just one line and the same String
print('hello world \'single code in side single code\'') # backslash allows also printing the word with single Quotes
line_Feed= "/n"
print("First Line \n Second Line") # print second line in another line
carriage_return= "\r"
print("123456\ruser")
horizontal_tap= "\t"
print("hello\tworld")
hex_value="\x4F"
print(hex_value)


