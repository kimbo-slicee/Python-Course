# String Concatenation:
#----------------------
# String concatenation means add strings together.
# Use the + character to add a variable to another variable:
x = "Python is "
y = "awesome"
z =  x + y
print(z)
print(z + "that's true")
# To add a space between them, add a " ":
a = "Hello"
b = "World"
c = a + " " + b
print(c)
